package com.venadostest.module.statistics.models;

public class StatisticModel {
    public Integer position;
    public String image;
    public String team;
    public Integer games;
    public Integer win;
    public Integer loss;
    public Integer tie;
    public Integer f_goals;
    public Integer a_goals;
    public Integer score_diff;
    public Integer points;
    public Object efec;

    public Integer getPosition() {
        return position;
    }

    public void setPosition(Integer position) {
        this.position = position;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getTeam() {
        return team;
    }

    public void setTeam(String team) {
        this.team = team;
    }

    public Integer getGames() {
        return games;
    }

    public void setGames(Integer games) {
        this.games = games;
    }

    public Integer getWin() {
        return win;
    }

    public void setWin(Integer win) {
        this.win = win;
    }

    public Integer getLoss() {
        return loss;
    }

    public void setLoss(Integer loss) {
        this.loss = loss;
    }

    public Integer getTie() {
        return tie;
    }

    public void setTie(Integer tie) {
        this.tie = tie;
    }

    public Integer getF_goals() {
        return f_goals;
    }

    public void setF_goals(Integer f_goals) {
        this.f_goals = f_goals;
    }

    public Integer getA_goals() {
        return a_goals;
    }

    public void setA_goals(Integer a_goals) {
        this.a_goals = a_goals;
    }

    public Integer getScore_diff() {
        return score_diff;
    }

    public void setScore_diff(Integer score_diff) {
        this.score_diff = score_diff;
    }

    public Integer getPoints() {
        return points;
    }

    public void setPoints(Integer points) {
        this.points = points;
    }

    public Object getEfec() {
        return efec;
    }

    public void setEfec(Object efec) {
        this.efec = efec;
    }
}
